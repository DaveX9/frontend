// Function to change language
function changeLanguage(lang) {
  document.querySelectorAll("[data-translate]").forEach(el => {
    const key = el.getAttribute("data-translate");
    el.textContent = translations[lang][key];
  });
}

// Event listeners for language switcher
document.querySelector('.lang-link[href="?lang=en"]').addEventListener('click', (e) => {
  e.preventDefault();
  changeLanguage("en");
});

document.querySelector('.lang-link[href="?lang=th"]').addEventListener('click', (e) => {
  e.preventDefault();
  changeLanguage("th");
});

// Set default language to Thai
changeLanguage("th");